    if (year < 1900 || year > 2100) return 0; // ‡∏õ‡∏µ‡πÑ‡∏°‡πà‡∏™‡∏°‡πÄ‡∏´‡∏ï‡∏∏‡∏™‡∏°‡∏ú‡∏•
